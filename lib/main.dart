    SystemChrome.setSystemUIOverlayStyle(const SystemUiOverlayStyle(
        // statusBarColor: Colors.transparent,
        // systemNavigationBarColor: Colors.black,
        ));
    return ScreenUtilInit(
      designSize: const Size(375, 812),
      minTextAdapt: true,
